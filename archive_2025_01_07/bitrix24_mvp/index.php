# Smart Document Generator MVP для Битрикс24

## Введение
Главный файл приложения `index.php` реализует интерфейс генерации документов с AI-ассистентом и интеграцией с CRM Битрикс24. Решение позволяет автоматизировать создание договоров, актов и других документов на основе данных CRM.

